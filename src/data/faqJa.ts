/** 全40問を質問文で引けるように索引化（設問文はサイト内で一意） */
const BY_QUESTION = new Map(JA_FAQ_SECTIONS.flatMap((s) => s.items).map((it) => [it.q, it]));

/**
 * 質問文をキーにB-3の設問を取り出す（業務ページへの転載用）。
 * 見つからない場合はビルド時に例外を投げる＝設問文を書き換えたのに参照側を直し忘れた事故を検知する。
 */
export function pickFaqJa(questions: string[]): FaqItem[] {
  return questions.map((q) => {
    const item = BY_QUESTION.get(q);
    if (!item) {
      throw new Error(
        `[faqJa] 質問文が JA_FAQ_SECTIONS に見つかりません: "${q}"（設問文の変更時は参照側も更新すること）`,
      );
    }
    return item;
  });
}
